export type IEmailAddress = string & { EmailAddress: "EmailAddress" };

//source: https://emailregex.com/

const EMAIL_ADDRESS_REGEX = /(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/;

export const validateEmailAddress = (emailAddress: string) => {
  const errors = [];

  if (!emailAddress.match(EMAIL_ADDRESS_REGEX)) {
    errors.push(new Error("Invalid email address"));
  }

  return errors;
};

export const EmailAddress = (emailAddress: string) => {
  const errors = validateEmailAddress(emailAddress);
  if (errors.length === 0) {
    return emailAddress as IEmailAddress;
  }
  throw new Error(errors.toString());
};
